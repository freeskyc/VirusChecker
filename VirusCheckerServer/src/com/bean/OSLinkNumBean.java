package com.bean;

public class OSLinkNumBean {
	private int sid;
	private int number;
	
	public OSLinkNumBean(int sid, int number) {
		super();
		this.sid = sid;
		this.number = number;
	}
	
	public int getSid() {
		return sid;
	}
	
	public void setSid(int sid) {
		this.sid = sid;
	}
	
	public int getNumber() {
		return number;
	}
	
	public void setNumber(int number) {
		this.number = number;
	}
	
}
